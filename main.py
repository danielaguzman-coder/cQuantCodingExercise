# ============================================================
# TASK 4 — Price Volatility - Hourly volatility is defined as the standard deviation in log returns of hourly prices.
# ============================================================
df_hubs = df[
    df["SettlementPoint"].str.startswith("HB_")
].copy()

# (log is only defined for positive values)
df_hubs = df_hubs[df_hubs["Price"] > 0]

df_hubs = df_hubs.sort_values(["SettlementPoint", "Date"]).reset_index(drop=True)
df_hubs["Year"]     = df_hubs["Date"].dt.year
df_hubs["TimeDiff"] = df_hubs.groupby("SettlementPoint")["Date"].diff().dt.total_seconds() / 3600
df_hubs["LogReturn"] = df_hubs.groupby("SettlementPoint")["Price"].transform(
    lambda s: np.log(s).diff())

# Set log return to NaN at gap points to avoid inflated volatility

df_hubs.loc[df_hubs["TimeDiff"] > 1, "LogReturn"] = np.nan
df_hubs = df_hubs.dropna(subset=["LogReturn"])

volatility_df = (
    df_hubs.groupby(["SettlementPoint", "Year"])["LogReturn"]
    .std().reset_index(name="HourlyVolatility")
)

print(f"\nVolatility computed")
print(volatility_df.to_string(index=False))

# ============================================================
# TASK 5 — Save Volatility
# ============================================================
