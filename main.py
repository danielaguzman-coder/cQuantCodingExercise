# ============================================================
# BONUS — Open-Ended Analysis
#
# Power prices alone can reveal a great deal about a region's
# energy dynamics. Without knowing exact supply or demand figures,
# price patterns expose structural strengths and weaknesses of
# each locality — from grid congestion and fuel mix to weather
# sensitivity and infrastructure constraints.
# Below we explore how each settlement hub behaves across the
# four seasons, revealing where and when price spikes concentrate.
# ============================================================

df["Hour"]      = df["Date"].dt.hour
df["DayOfWeek"] = df["Date"].dt.dayofweek
df["Year"]      = df["Date"].dt.year
df["Month"]     = df["Date"].dt.month

def get_season(month):
    if month in [12, 1, 2]:  return "Winter"
    elif month in [3, 4, 5]: return "Spring"
    elif month in [6, 7, 8]: return "Summer"
    else:                     return "Fall"

df["Season"] = df["Month"].apply(get_season)

season_order  = ["Winter", "Spring", "Summer", "Fall"]
season_colors = {"Winter": "#3498db", "Spring": "#2ecc71",
                 "Summer": "#e74c3c", "Fall":   "#e67e22"}

hubs = sorted(df[df["SettlementPoint"].str.startswith("HB_")]["SettlementPoint"].unique())
df_hubs = df[df["SettlementPoint"].str.startswith("HB_")].copy()

# ---- Plot 1: Average price by hub and season ----
# Shows which hubs are most expensive in each season
# revealing seasonal supply/demand imbalances per locality
seasonal_avg = (
    df_hubs.groupby(["SettlementPoint", "Season"])["Price"]
    .mean().reset_index()
)
seasonal_avg["Hub"] = seasonal_avg["SettlementPoint"].str.replace("HB_", "")

fig, axes = plt.subplots(1, 4, figsize=(18, 6), sharey=True)
for i, season in enumerate(season_order):
    data = seasonal_avg[seasonal_avg["Season"] == season].sort_values("Price", ascending=False)
    axes[i].barh(data["Hub"], data["Price"],
                 color=season_colors[season], alpha=0.85, edgecolor="white")
    axes[i].set_title(season, fontsize=13, fontweight="bold", color=season_colors[season])
    axes[i].set_xlabel("Avg Price ($/MWh)", fontsize=10)
    axes[i].xaxis.set_major_formatter(mticker.FormatStrFormatter('$%.0f'))
    if i == 0:
        axes[i].set_ylabel("Settlement Hub", fontsize=11)
    sns.despine(ax=axes[i])

fig.suptitle("Average Price by Settlement Hub and Season\nWhich hubs are most expensive and when?",
             fontsize=14, fontweight="bold", y=1.02)
plt.tight_layout()
plt.savefig("outputs/visualizations/HubAveragePriceBySeason.png", dpi=175, bbox_inches="tight")
plt.show()

# ---- Plot 2: Price spike magnitude by hub and season ----
# A spike is defined as any hour above the 95th percentile
# We look at HOW MUCH prices spike, not just how often
# This reveals which localities are most vulnerable to extreme prices
spike_threshold = df_hubs["Price"].quantile(0.95)
df_spikes = df_hubs[df_hubs["Price"] > spike_threshold].copy()

spike_magnitude = (
    df_spikes.groupby(["SettlementPoint", "Season"])["Price"]
    .mean().reset_index(name="AvgSpikePrice")
)
spike_magnitude["Hub"] = spike_magnitude["SettlementPoint"].str.replace("HB_", "")

spike_pivot = spike_magnitude.pivot(index="Hub", columns="Season", values="AvgSpikePrice")
spike_pivot = spike_pivot[season_order]

fig, ax = plt.subplots(figsize=(12, 7))
sns.heatmap(spike_pivot, annot=True, fmt=".0f", cmap="YlOrRd",
            linewidths=0.5, ax=ax,
            cbar_kws={"label": "Average Spike Price ($/MWh)"},
            annot_kws={"size": 11})
ax.set_title(f"Average Spike Price by Hub and Season\n(Spike = price above 95th percentile: ${spike_threshold:.0f}/MWh)\nHigher values = more severe price spikes",
             fontsize=13, fontweight="bold", pad=15)
ax.set_xlabel("")
ax.set_ylabel("")
plt.tight_layout()
plt.savefig("outputs/visualizations/HubSpikeMagnitudeBySeason.png", dpi=175, bbox_inches="tight")
plt.show()

# ---- Plot 3: Spike frequency by hub and season ----
# How often does each hub spike in each season?
# Combined with Plot 2 this gives a full picture:
# frequent + large spikes = structurally weak locality
spike_freq = (
    df_hubs.groupby(["SettlementPoint", "Season"])
    .apply(lambda x: (x["Price"] > spike_threshold).mean())
    .reset_index(name="SpikeFrequency")
)
spike_freq["Hub"] = spike_freq["SettlementPoint"].str.replace("HB_", "")

freq_pivot = spike_freq.pivot(index="Hub", columns="Season", values="SpikeFrequency")
freq_pivot = freq_pivot[season_order]

fig, ax = plt.subplots(figsize=(12, 7))
sns.heatmap(freq_pivot, annot=True, fmt=".1%", cmap="YlOrRd",
            linewidths=0.5, ax=ax,
            cbar_kws={"label": "Spike Frequency (% of hours)"},
            annot_kws={"size": 11})
ax.set_title("Price Spike Frequency by Hub and Season\n(% of hours above 95th percentile)\nHigher values = spikes happen more often",
             fontsize=13, fontweight="bold", pad=15)
ax.set_xlabel("")
ax.set_ylabel("")
plt.tight_layout()
plt.savefig("outputs/visualizations/HubSpikeFrequencyBySeason.png", dpi=175, bbox_inches="tight")
plt.show()

# ---- Plot 4: Intraday spike pattern by season ----
# At what time of day do spikes happen in each season?
# This reveals the structural drivers:
# Summer afternoon spikes = AC demand
# Winter morning spikes = heating demand
# These patterns are critical for scheduling and hedging decisions
spike_hour = (
    df_hubs[df_hubs["Price"] > spike_threshold]
    .groupby(["Season", "Hour"])["Price"]
    .count().reset_index(name="SpikeCount")
)
total_by_season = df_hubs.groupby("Season")["Price"].count().reset_index(name="Total")
spike_hour = spike_hour.merge(total_by_season, on="Season")
spike_hour["SpikeRate"] = spike_hour["SpikeCount"] / spike_hour["Total"]

fig, axes = plt.subplots(1, 4, figsize=(18, 5), sharey=True)
for i, season in enumerate(season_order):
    data = spike_hour[spike_hour["Season"] == season]
    axes[i].fill_between(data["Hour"], data["SpikeRate"],
                         color=season_colors[season], alpha=0.7)
    axes[i].plot(data["Hour"], data["SpikeRate"],
                 color=season_colors[season], linewidth=2)
    axes[i].set_title(season, fontsize=13, fontweight="bold", color=season_colors[season])
    axes[i].set_xlabel("Hour of Day", fontsize=10)
    axes[i].set_xticks([0, 6, 12, 18, 23])
    axes[i].yaxis.set_major_formatter(mticker.PercentFormatter(xmax=1, decimals=1))
    if i == 0:
        axes[i].set_ylabel("Spike Rate", fontsize=11)
    sns.despine(ax=axes[i])

fig.suptitle("Intraday Price Spike Pattern by Season\nAt what time of day do spikes concentrate?",
             fontsize=14, fontweight="bold", y=1.02)
plt.tight_layout()
plt.savefig("outputs/visualizations/IntradaySpikePatternBySeason.png", dpi=175, bbox_inches="tight")
plt.show()

print("Open-ended analysis complete")

